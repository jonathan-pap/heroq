// =====================================================================
// install-canonical-batch.js
//
// Drives the entire canonical-XML-to-main-quest pipeline for every
// quest declared in data/quests/_meta.yaml. For each:
//
//   1. Locates the matching sandbox-canonical-qNN-….json (built earlier
//      by scripts/build-quest1-from-xml.js).
//   2. Applies the per-quest meta (id, title, intro, wandering monster,
//      boss override, friendly NPC, treasure values, objective).
//   3. Writes the result to data/quests/<id>.json (the main quest list).
//   4. Removes the sandbox copy (since canonical is now the main quest).
//
// Idempotent — run as often as you like to refresh quests after
// regenerating their sandbox JSON from XML.
//
// Run:  node scripts/install-canonical-batch.js
//       node scripts/install-canonical-batch.js q05    (single quest)
// =====================================================================

const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const META_PATH = path.join(__dirname, '..', 'data', 'quests', '_meta.yaml');
const SANDBOX_DIR = path.join(__dirname, '..', 'data', 'quests', 'sandbox');
const MAIN_DIR = path.join(__dirname, '..', 'data', 'quests');

const ONLY = process.argv[2];   // optional 'qNN' filter

const meta = yaml.load(fs.readFileSync(META_PATH, 'utf8')).quests || {};

// Find a sandbox file matching qNN (e.g. q01) — filename can be any of
// sandbox-canonical-q01-… so we just glob-match the prefix.
function findSandboxFor(qKey) {
  const prefix = `sandbox-canonical-${qKey}-`;
  if (!fs.existsSync(SANDBOX_DIR)) return null;
  const files = fs.readdirSync(SANDBOX_DIR);
  const hit = files.find(f => f.startsWith(prefix) && f.endsWith('.json'));
  return hit ? path.join(SANDBOX_DIR, hit) : null;
}

function applyMeta(q, m) {
  // ---- 1. Identity ---------------------------------------------------
  q.id = m.id;
  q.title = m.title;
  q.subtitle = m.subtitle || '';
  q.category = 'main';
  q.intro = (m.intro || '').trim();
  q._canonical_notes =
    `Auto-generated by scripts/install-canonical-batch.js from the ` +
    `HeroScribe XML + per-quest meta in data/quests/_meta.yaml. ` +
    `Re-run the converter on the XML, then re-run this batch installer ` +
    `to refresh.`;
  q.wanderingMonster = m.wandering || 'orc';
  // Per-quest canonical rule notes (special trap behaviours, hidden
  // treasures, NPC alarms, etc). Captured from the rulebook so we
  // don't lose them when full-mechanics work lands later.
  if (m.quirks && m.quirks.length) q._quirks = m.quirks;

  // ---- 2. Boss override ---------------------------------------------
  if (m.bossOverride) {
    const b = m.bossOverride;
    const target = q.monsters.find(mo =>
      mo.type === b.type && mo.at[0] === b.at[0] && mo.at[1] === b.at[1]
    );
    if (target) {
      if (b.idAlias)  target.id = b.idAlias;
      if (b.name)     target.name = b.name;
      if (b.attack != null) target.attack = b.attack;
      if (b.defend != null) target.defend = b.defend;
      if (b.body != null) {
        target.body = b.body; target.bodyMax = b.body;
      }
      if (b.mind != null) {
        target.mind = b.mind; target.mindMax = b.mind;
      }
      target._note = `${m.title} boss — canonical override.`;
    } else {
      console.warn(`  ${m.id}: boss override target not found at ` +
                   `${b.at[0]},${b.at[1]} (type=${b.type})`);
    }
  }

  // ---- 3. Monster id aliases (rename auto-generated ids to stable
  //        ones referenced by objectives) ----------------------------
  if (m.monsterIdAlias) {
    for (const [oldId, newId] of Object.entries(m.monsterIdAlias)) {
      const target = q.monsters.find(mo => mo.id === oldId);
      if (target) target.id = newId;
    }
  }

  // ---- 4. Friendly NPC ---------------------------------------------
  if (m.friendlyNpc) {
    q.friendlyNpc = { ...m.friendlyNpc };
  }

  // ---- 5. Treasure overrides ---------------------------------------
  if (m.treasure) {
    q.treasure = q.treasure.map(t => {
      const key = `${t.at[0]},${t.at[1]}`;
      const o = m.treasure[key];
      if (!o) return t;
      const out = { ...t };
      if (o.amount != null) out.amount = o.amount;
      if (o.note) out._note = o.note;
      return out;
    });
  }

  // ---- 6. Objective + objectives -----------------------------------
  if (m.objective) {
    q.objective = { ...m.objective };
    // Auto-build a single-step objectives list if none authored yet.
    q.objectives = [{
      id: m.objective.kind === 'kill' ? 'slay-boss'
         : m.objective.kind === 'reach' ? 'reach-goal'
         : m.objective.kind === 'escort' ? 'escort-npc'
         : 'goal',
      ...m.objective,
    }];
  }
  if (m.defeat) {
    q.defeat = { kind: 'all-dead', text: m.defeat };
  }
  return q;
}

function processQuest(qKey, m) {
  const sandbox = findSandboxFor(qKey);
  if (!sandbox) {
    console.warn(`SKIP ${qKey}: no sandbox-canonical-${qKey}-… file found`);
    return false;
  }
  const q = JSON.parse(fs.readFileSync(sandbox, 'utf8'));
  applyMeta(q, m);
  const outPath = path.join(MAIN_DIR, `${m.id}.json`);
  fs.writeFileSync(outPath, JSON.stringify(q, null, 2) + '\n');
  // Remove the sandbox copy now that canonical is the main quest.
  fs.unlinkSync(sandbox);
  console.log(
    `OK   ${qKey} → ${m.id}.json  ` +
    `(${q.monsters.length} monsters, ${q.furniture.length} furniture, ` +
    `${q.doors.length} doors, ${(q.secretDoors || []).length} secret, ` +
    `${(q.traps || []).length} traps, ${q.treasure.length} chests)`
  );
  return true;
}

let ok = 0, skip = 0;
for (const [qKey, m] of Object.entries(meta)) {
  if (ONLY && qKey !== ONLY) continue;
  if (processQuest(qKey, m)) ok++; else skip++;
}
console.log(`\n${ok} installed, ${skip} skipped.`);
